          <h2 class="entry-title">
            <a href="#">Sejarah Makassar</a>
          </h2>

          <div class="entry-content">
            <p>
              Awal kota dan bandar Makassar berada di muara Sungai Tallo dengan
              pelabuhan niaga kecil di wilayah itu pada penghujung abad XV. Sumbersumber Portugis memberitakan, bahwa bandar Tallo itu awalnya berada di
              bawah Kerajaan Siang di sekitar Pangkajene. Pada pertengahan abad XVI,
              Tallo bersatu dengan sebuah kerajaan kecil lainnya yang bernama Gowa, dan
              mulai melepaskan diri dari kerajaan Siang, bahkan menyerang dan
              menaklukkan kerajaan-kerajaan sekitarnya.
            </p>

            <p>
              Akibat semakin intensifnya kegiatan pertanian di hulu sungai Tallo,
              mengakibatkan pendangkalan sungai Tallo, sehingga bandarnya
              dipindahkan ke muara sungai Jeneberang, di sinilah terjadi pembangunan
              kekuasaan kawasan istana oleh para ningrat Gowa-Tallo yang kemudian
              membangun pertahanan benteng Somba Opu, yang seratus tahun kemudian
              menjadi wilayah inti Kota Makassar. Pada masa pemerintahan Raja Gowa
              XVI, didirikan Benteng Rotterdam, pada masa itu terjadi peningkatan
              aktivitas pada sektor perdagangan lokal, regional dan internasional, sektor
              politik serta sektor pembangunan fisik oleh kerajaan. Masa itu merupakan
              puncak kejayaan Kerajaan Gowa, namun selanjutnya dengan adanya
              perjanjian Bungaya menghantarkan Kerajaan Gowa pada awal keruntuhan. 
            </p>

