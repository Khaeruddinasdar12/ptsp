            <p>
              Komoditi ekspor utama Makassar adalah beras, yang dapat ditukar
              dengan rempah-rempah dari Maluku maupun barang-barang manufaktur
              asal Timur Tengah, India dan Cina di Nusantara Barat. Dari laporan saudagar
              Portugal maupun catatan-catatan lontara setempat, diketahui bahwa
              peranan penting saudagar Melayu dalam perdagangan yang berdasarkan
              pertukaran hasil pertanian dengan barang-barang impor. Dengan
              menaklukkan kerajaan-kerajaan kecil di sekitarnya, yang pada umumnya
              berbasis agraris, maka Makassar menguasai kawasan pertanian yang relatif
              luas dan berusaha pula untuk membujuk para saudagar di kerajaan
              sekitarnya agar pindah ke Makassar, sehingga kegiatan perdagangan
              semakin terkonsentrasi di bandar niaga baru Makassar.

            </p>

            <p>
              Hanya dalam seabad saja, Makassar menjadi salah satu kota niaga
              terkemuka dunia yang dihuni lebih 100.000 orang (kota terbesar ke 20
              dunia). Pada zaman itu jumlah penduduk Amsterdam, yang termasuk kota
              kosmopolitan dan multikultural baru mencapai sekitar 60.000 orang.
              Perkembangan bandar Makassar yang demikian pesat itu, berkat
              hubungannya dengan perubahan-perubahan pada tatanan perdagangan
              internasional masa itu. Pusat utama jaringan perdagangan di Malaka,
              ditaklukkan oleh Portugal pada tahun 1511, demikian juga di Jawa Utara
              semakin berkurang mengikuti kekalahan armada lautnya di tangan Portugal
              dan pengkotak-kotakan dengan kerajaan Mataram. Bahkan ketika Malaka
              diambil alih oleh Kompeni Dagang Belanda (VOC) pada tahun 1641, banyak
              pedagang Portugis ikut pindah ke Makassar.
            </p>

            <p>
             Sampai pada pertengahan abad ke-17, Makassar berupaya
             merentangkan kekuasaannya ke sebagian besar Indonesia Timur dengan
             menaklukkan Pulau Selayar dan sekitarnya, kerajaan-kerajaan Wolio di
             Buton, Bima di Sumbawa, Banggai dan Gorontalo di Sulawesi bagian Timur
             dan Utara serta mengadakan perjanjian dengan kerajaan-kerajaan di Seram 
           </p>

           <p>
            Hubungan Makassar dengan dunia Islam diawali dengan kehadiran
            Abdul Ma’mur Khatib Tunggal atau Dato’ Ri Bandang yang berasal dari
            Minangkabau, Sumatera Barat yang tiba di Tallo (sekarang Makassar) pada
            bulan September 1605. Beliau mengislamkan Raja Gowa ke-XIV IMANGNGARANGI DAENG MANRABIA dengan gelar SULTAN ALAUDDIN
            (memerintah Tahun 1593-1639), dan dengan Mangkubumi IMALLINGKAANG DAENG MANYONRI KARAENG KATANGKA yang juga
            sebagai Raja Tallo. Kedua raja ini, yang mulai memeluk Agama Islam di
            Sulawesi Selatan. Pada tanggal 9 Nopember 1607, tepatnya hari Jum’at,
            diadakan shalat Jum’at pertama di Mesjid Tallo dan dinyatakan secara resmi
            bahwa penduduk Kerajaan Gowa-Tallo telah memeluk Agama Islam, pada
            waktu bersamaan pula, diadakan shalat Jum’at di Mesjid Mangallekana di
            Somba Opu. Tanggal inilah yang selanjutnya diperingati sebagai Hari Jadi
            Kota Makassar sejak Tahun 2000, yang sebelumnya hari jadi kota Makassar
            diperingati pada tanggal 1 April setiap tahunnya. Para ningrat Makassar dan
            rakyatnya dengan giat ikut dalam jaringan perdagangan internasional, dan
            interaksi dengan komunitas kota yang kosmopolitan itu menyebabkan
            sebuah “creative renaissance” yang menjadikan Bandar Makassar sebagai
            salah satu pusat ilmu pengetahuan terdepan pada zamannya. Koleksi buku
            dan peta, zaman itu masih langka di Eropa namun di Makassar sudah banyak
            terkumpul. Makassar merupakan salah satu perpustakaan ilmiah terbesar di
            dunia, dan para sultan tak segan-segan memesan barang-barang paling
            mutakhir dari seluruh pelosok bumi, termasuk bola dunia dan teropong
            terbesar pada waktunya, yang dipesan secara khusus dari Eropa. Ambisi para
            pemimpin Kerajaan Gowa-Tallo untuk semakin memperluas wilayah
            kekuasaan serta persaingan Bandar Makassar dengan Kompeni Dagang
            Belanda (VOC) berakhir dengan perang paling dahsyat dan sengit yang
            pernah dijalankan Kompeni. Pasukan Bugis, Belanda dan sekutunya dari
            Ternate, Buton dan Maluku memerlukan tiga tahun operasi militer di seluruh
            kawasan Indonesia Timur. Baru pada Tahun 1669, akhirnya dapat meratatanahkan kota Makassar dan benteng terbesarnya, Somba Opu. Bagi
            Sulawesi Selatan, kejatuhan Makassar di tangan federasi itu merupakan
            sebuah titik balik yang berarti bahwa Bandar Niaga Makassar menjadi
            wilayah kekuasaan VOC, dan beberapa pasal perjanjian perdamaian
            membatasi dengan ketat kegiatan pelayaran antar-pulau Gowa-Tallo dan
            sekutunya. Pelabuhan Makassar ditutup bagi pedagang asing, sehingga
            komunitas saudagar hijrah ke pelabuhan-pelabuhan lain. Pada beberapa
            dekade pertama setelah pemusnahan kota dan bandar Makassar, penduduk
            yang tersisa membangun sebuah pemukiman baru di sebelah utara bekas
            Benteng Ujung Pandang, benteng pertahanan pinggir utara kota lama itu
            pada Tahun 1673 ditata ulang oleh VOC sebagai pusat pertahanan dan
            pemerintahan diberi nama baru Fort Rotterdam, dan ‘kota baru’ yang mulai
            tumbuh di sekelilingnya itu dinamakan ‘Vlaardingen’. Pemukiman itu jauh
            lebih kecil daripada Kota Raya Makassar yang telah dihancurkan. Pada
            dekade pertama seusai perang, seluruh kawasan itu dihuni tidak lebih 2.000
            jiwa, pada pertengahan abad ke-18 jumlah itu meningkat menjadi sekitar
            5.000 orang, setengah di antaranya berupa budak. Selama dikuasai VOC,
            Makassar menjadi sebuah kota yang terlupakan, maupun para penjajah
            kolonial pada abad ke-19 itu tak mampu menaklukkan jazirah Sulawesi
            Selatan yang sampai awal abad ke-20 masih terdiri dari lusinan kerajaan
            kecil yang independen dari pemerintahan asing, bahkan sering harus
            mempertahankan diri terhadap serangan militer yang dilakukan kerajaankerajaan itu. Maka, ‘Kota Kompeni’ itu hanya berfungsi sebagai pos
            pengamanan di jalur utara perdagangan rempahrempah tanpa hinterland
            bentuknya pun bukan ‘bentuk kota’, tetapi suatu aglomerasi kampungkampung di pesisir pantai sekeliling Fort Rotterdam.
          </p>

          <p>
            Pada awalnya, kegiatan perdagangan utama beras di Bandar Dunia ini
            adalah pemasaran budak serta suplai beras kepada kapal¬kapal VOC dan
            menukarkannya dengan rempah-rempah di Maluku. Pada tahun 30-an di
            abad ke-18, pelabuhan Makassar dibuka bagi kapal-kapal dagang Cina.
            Komoditi yang dicari para saudagar Tionghoa di Sulawesi, pada umumnya
            berupa hasil laut dan hutan seperti teripang, sisik penyu, kulit kerang,
            sarang burung dan kayu cendana, sehingga tidak dianggap sebagai
            langganan dan persaingan bagi monopoli jual-beli rempah-rempah dan kain
            yang didirikan VOC. Sebaliknya, barang dagangan Cina, terutama porselen
            dan kain sutera, dijual para saudagarnya dengan harga yang lebih murah di
            Makassar daripada yang bisa didapat oleh pedagang asing di negeri Cina
            sendiri. Adanya pasaran baru itu, mendorong kembali aktivitas maritim
            penduduk kota dan kawasan Makassar. Terutama penduduk pulau-pulau di
            kawasan Spermonde mulai menspesialisasikan diri sebagai pencari teripang,
            komoditi utama yang dicari para pedagang Cina, dengan menjelajahi seluruh
            Kawasan Timur Nusantara. Sejak pertengahan abad ke-18 para nelayanpelaut Sulawesi secara rutin berlayar hingga pantai utara Australia, selama
            tiga sampai empat bulan lamanya membuka puluhan lokasi pengolahan
            teripang. Sampai sekarang, hasil laut masih merupakan salah satu mata
            pencaharian utama bagi penduduk pulau-pulau dalam wilayah Kota
            Makassar. Setetah Pemerintah Kolonial Hindia Belanda menggantikan
            kompeni perdagangan VOC yang bangkrut pada akhir abad ke-18, Makassar
            dihidupkan kembali dengan menjadikannya sebagai pelabuhan bebas pada
            tahun 1846. Tahun-tahun berikutnya terjadi kenaikan volume perdagangan
            yang pesat, dan kota Makassar berkembang dari sebuah pelabuhan
            backwater kembali menjadi bandar internasional. Dengan semakin
            berputarnya roda perekonornian Makassar, jumlah penduduknya meningkat
            dari sekitar 15.000 penduduk pada pertengahan abad ke-19 menjadi kurang
            lebih 30.000 jiwa pada awal abad berikutnya. Makassar abad ke-19 itu
            dijuluki “kota kecil terindah di seluruh Hindia-Belanda” (Joseph Conrad,
            seorang penulis Inggris-Polandia terkenal),dan menjadi salah satu port of call
            utama bagi para pelaut pedagang Eropa, India dan Arab dalam pemburuan
            hasil-hasil hutan yang amat laku di pasaran dunia maupun perahu-perahu
            pribumi yang beroperasi di antara Jawa, Kalimantan, Sulawesi dan Maluku.
            Pada awal abad ke-20, Belanda akhirnya menaklukkan daerah-daerah
            independen di Sulawesi, Makassar dijadikan sebagai pusat pemerintahan
            kolonial Indonesia Timur. Tiga setengah dasawarsa Neerlandica, kedamaian
            di bawah pemerintahan kolonial itu adalah masa tanpa perang paling lama
            yang pernah dialami Sulawesi Selatan, dan sebagai akibat ekonominya
            berkembang dengan pesat. Penduduk Makassar dalam kurun waktu itu
            meningkat sebanyak tiga kali lipat, dan wilayah kota diperluas ke semua
            penjuru. Dideklarasikan sebagai Kota Madya pada tahun 1906, Makassar
            tahun 1920-an adalah kota besar kedua di luar Jawa yang membanggakan
            dirinya dengan sembilan perwakilan asing, sederetan panjang toko di tengah
            kota yang menjual barang-barang mutakhir dari seluruh dunia dan
            kehidupan sosial-budaya yang dinamis dan kosmopolitan. Perang Dunia
            Kedua dan pendirian Republik Indonesia sekali lagi mengubah wajah
            Makassar. Hengkangnya sebagian besar warga asing pada Tahun 1949 dan
            Nasionalisasi perusahaan-perusahaan asing pada akhir Tahun 1950-an
            menjadikannya kembali sebuah kota provinsi. Bahkan, sifat asli Makassar
            pun semakin menghilang dengan kedatangan warga baru dari daerah-daerah
            pedalaman yang berusaha menyelamatkan diri dari kekacauan akibat
            berbagai pergolakan pasca revolusi.
          </p>

          <p>
            Antara Tahun 1930-an sampai Tahun 1961 jumlah penduduk
            meningkat dari kurang lebih 90.000 jiwa menjadi hampir 400.000 orang,
            lebih daripada setengahnya pendatang baru dari wilayah luar kota. Hal ini
            dicerminkan dalam penggantian nama kota menjadi Ujung Pandang
            berdasarkan julukan ”Jumpandang” yang selama berabad-abad lamanya
            menandai Kota Makassar bagi orang pedalaman pada Tahun 1971. Baru pada
            Tahun 1999 kota ini berubah nama kembali menjadi Makassar, tepatnya 13
            Oktober berdasarkan Peraturan Pemerintah Nomor 86 Tahun 1999 Nama
            Ujung Pandang dikembalikan menjadi Kota Makassar. Dan sesuai Undang Undang Nomor 22 Tahun 1999 tentang Pemerintahan Daerah yang diganti
            dengan Undang-undang Nomor 32 Tahun 2004, luas wilayah Kota Makassar
            bertambah kurang lebih 4 mil kearah laut setara dengan 10.000 Ha, sehingga
            seluruh daratan dan lautan seluas ± 27.577 Ha.
          </p>

